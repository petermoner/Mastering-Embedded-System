#ifndef LSTD_TYPES_H_
#define LSTD_TYPES_H_


/************************************************************************************************/
/*                                    Standard data types                                       */
/************************************************************************************************/

/*1 byte data types*/
typedef unsigned char  u8_t;
typedef signed char    s8_t;

/*2 bytes data types*/
typedef unsigned short u16_t;
typedef signed short   s16_t;

/*4 bytes data types*/
typedef unsigned short u32_t;
typedef signed short   s32_t;

/*Floating data types*/
typedef float          f32_t;
typedef double         f64_t;



#endif /* LSTD_TYPES_H_ */